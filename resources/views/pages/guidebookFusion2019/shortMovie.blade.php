                        <h2>Rules and Regulations</h2>
                        <ol>
                            <li>Short movie competition is open to all high school students in Indonesia.</li>
                            <li>Participants must form a team that consists of 4-7 students from the same high school.</li>
                            <li>Participants must attach a letter of recommendation from their school to join the competition.</li>
                            <li>Short movie duration should be within 5-10 minutes.</li>
                            <li>The short movie must be video footage or animation. Slideshows with music are not acceptable.</li>
                            <li>The Short movie format resolution must be at least 1080p, free ratio aspect, and has an adequate sound quality.</li>
                            <li>The short movie must have English subtitles.</li>
                            <li>The Short movie must be appropriate for all audiences (no profanity or mature content and should not contain elements of SARA).</li>
                            <li>The Short movie is an original film made by the team, authentic, and has never been published before.</li>
                            <li>The Short movie must contain credits to all participants in the movie (crew, actors, songs, etc.)</li>
                            <li>Captions/subtitles must be in English</li>
                            <li>Sponsor logos and IEEE logo must be included at the beginning of the video, it can be downloaded at IEEE ITB SB Website</li>
                        </ol>
                    </div>
                </div>

